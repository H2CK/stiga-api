const EventEmitter = require('events');

// ------------------------------------------------------------------------------------------------------------------------------------------------------------
// ------------------------------------------------------------------------------------------------------------------------------------------------------------

class StigaAPIComponent extends EventEmitter {
    constructor(options) {
        super();
        this.debug = Boolean(options?.debug);
        this.verbose = Boolean(options?.verbose);
        this.display = {
            log: (...args) => (options.logger || console.log)(...args),
            warn: (...args) => (options.logger || console.warn)(...args),
            error: (...args) => (options.logger || console.error)(...args),
            debug: (...args) => {
                if (this.debug) (options.logger || console.log)('[DEBUG]', ...args);
            },
            verbose: (...args) => {
                if (this.verbose || this.debug) (options.logger || console.log)('[VERBOSE]', ...args);
            },
        };
    }
    //
}

// ------------------------------------------------------------------------------------------------------------------------------------------------------------
// ------------------------------------------------------------------------------------------------------------------------------------------------------------

module.exports = StigaAPIComponent;

